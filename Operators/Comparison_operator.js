// <----------- Comparison Operator ---------->

//   >
//   <
//   >=
// less than or equal to (<=)
// Not equals  !== 
// check both values and datatype are same strict  ===


// <--------- == Comparison Operator -------->

let name = '10';
let k = 10;

if (name == k) {  // true
    console.log ('Both values are same');
}
else {
    console.log ('Both values are not same');
}




// <--------- === Comparison Operator -------->

if (name === k) {  // false
    console.log ('Both values and datatype are same');
}
else {
    console.log ('Both values and datatype are not same');
}
